describe("#CheckForValidEntryPattern", function() {
  it("returns true when given valid character patterns", function() {
    expect(CheckForValidEntryPattern("\n |  |  |  |  |  |  |  |  |\n |  |  |  |  |  |  |  |  |\n\n")).toEqual(true);
  });

  it("returns false when given invalid character patterns", function() {
    expect(CheckForValidEntryPattern("\n")).toEqual(false);
  });

})
